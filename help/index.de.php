<h1>Hilfe bekommen</h1>

<p>
Benötigen Sie Hilfe bei der Nutzung von Fink? Hier sind Ihre Optionen.
</p>

<p>
<b>Dokumentation.</b>
Der <a href="../doc/index.php">Dokumentationsteil</a> dieser Webseite hat jede Menge nützlicher Dokumente. Natürlich sind diese Dokumente jederzeit im Aufbau und viele unvollständig, aber sie sind definitiv hilfreich.</p>

<p>
<b>Die FAQ.</b> Häufige Probleme und ihre Lösungen sind in der <a href="../faq/index.php">FAQ</a> dokumentiert.
</p>

<p>
<b>Die Verteilerlisten.</b> Wenn Sie ein Problem nicht selbst lösen können, stellen Sie die Frage auf der <a href="../lists/fink-beginners.php">fink-beginners</a>- oder der <a href="../lists/fink-users.php">fink-users</a>-Verteilerliste. Es ist ratsam, erst das <a href="http://sourceforge.net/mailarchive/forum.php?forum=fink-beginners">fink-beginners</a>- und <a href="http://sourceforge.net/mailarchive/forum.php?forum=fink-users">fink-users</a>-Archiv zu überprüfen - denn es ist ermüdend, immer die gleichen Fragen zu beantworten.</p>
<p>Wenn Sie ein Problem melden, gehen Sie sicher, dass Sie relevante Informationen einbeziehen - wir können Ihnen nicht helfen, wenn wir nicht wissen, was Ihr Problem ist. Einige Dinge, die nicht fehlen sollten: Fink-Version, Mac OS X-Version, Versionen relevanter Pakete, der Finkbefehl, der fehlgeschlagen ist, jegliche Fehlernachrichten, die sinnvoll aussehen. Geben Sie außerdem an, ob Sie Software von anderen Quellen in /usr/local installiert haben oder einen anderen Compiler verwenden (z.B. gcc 3).</p>

<p>
<b>Der IRC-Kanal.</b>
Es gibt einen <tt>#fink</tt>-Kanal auf dem <a href="http://freenode.net">freenode</a>-IRC-Netzwerk. Hier können Sie mit anderen Finknutzern und einigen Entwicklern chatten.</p>

