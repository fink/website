<h1>Участники проекта</h1>

<p>Поддержка проекта Fink осуществляется группой волонтеров. Каждый пакет Fink имеет координатора, обеспечивающего соответствующую работу пакета. Помимо этого, существует <a href="organization.php#core">основная рабочая группа fink</a>, которая работает над самой программой fink.
Веб-сайт должен обновляться. Пользователи нуждаются в поддержке, предоставляемой через наши списки рассылки и  канал IRC. Эти и многие другие задачи требуют значительных усилий и мы должны каким-то образом поблагодарить всех тех, кто посвятил свое свободное время содействию сообществу.
Данная страница содержит список участников, которые тем или иным образом внесли свой вклад в Fink.
</p>

<p>За прошедшее время столь большое количество людей помогли Fink, что представляется затруднительным упомянуть всех вас. Если вы полагаете, что должны быть включены в список, а вас в нем нет, отправьте нам сообщение электронной почтой и мы постараемся включить ваше имя при составлении списка в следующий раз. </p>
 
<p>Выделенные курсивом имена означают псевдонимы, используемые на канале IRC.</p>

