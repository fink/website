<h1>Mailing-Listen</h1>

<p>
Fink hat mehrere Diskussions-Listen. Alle Listen werden von SourceForge zur
Verfügung gestellt und von Geocrawler archiviert.
</p>
<p>
Alle Diskussions-Listen sind frei zugänglich, nachdem Sie sich angemeldet haben. Klicken Sie auf den Namen der Liste, die Sie interessiert und Sie erhalten mehr Informationen.
</p>
<p>
<em>Bitte kein cross-posting!</em> 
Entscheiden Sie sich für eine Liste, bevor Sie eine Nachricht schicken. 
Wenn Sie sich nicht sicher sind, ob Ihre Anfrage passend für die gewählte Liste ist, 
dann Fragen Sie einfach vorher nach.
</p>
