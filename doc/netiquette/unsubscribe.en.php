    <h2><a name="unsubscribing">4.1 How do I unsubscribe?</a></h2>
      
      <p>To unsubscribe, <b>don't</b> send a message to the list.</p>
      <ol>
        <li>Go to the <a href="/lists/index.php">Mailing Lists</a> page.  <p>Note:  don't use the MacOSX version  Internet Explorer.</p></li>
        <li>Select the link for the list from which you wish to unsubscribe.</li>
        <li>Go to the bottom of the page where it says:  <q>To change your subscription (set options like digest and delivery modes, get a reminder of your password, or unsubscribe), enter your subscription email address:</q>, and enter the email address where you are receiving the Fink mail, then click the <q>Edit Options</q> button.   </li>
        <li>On the page that opens up next, there's a field in the upper left where you can unsubscribe.</li>
      </ol>
    
  
