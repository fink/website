<h1>Netiquette - 4. Abonnement kündigen</h1>
    
    
    <h2><a name="unsubscribing">4.1 Wie kündige ich das Abonnement einer Mailing-Liste?</a></h2>
      
      <p>Für eine Kündigung schickt man <b>keine</b> Nachricht an die Mailing-Liste.</p>
      <ol>
        <li>Gehen sie auf die Seite <a href="/lists/index.php">Mailing Lists</a>.
          <p>Notiz:  Aber nicht mit der Mac OS X Version des Internet Explorer.</p>
        </li>
        <li>Wählen sie den Link für die Liste aus, deren Abonnement sie kündigen wollen.</li>
        <li>Gehen sie auf der Seite ganz nach unten, wo folgendes steht:  <q>To change your subscription (set options like digest and delivery modes, get a reminder of your password, or unsubscribe), enter your subscription email address:</q>. Geben sie ihre Email-Adresse ein, auf der sie die Fink Mail erhalten und klicken sie auf <q>Edit Options</q> .
        </li>
        <li>Auf der Seite, die sich öffnet, gibt es oben links ein Feld, wo sie kündigen (unsubscribe) können.</li>
      </ol>
    
  
